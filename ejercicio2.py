### Variables

texto = "Curso de Programación en Python"
print(texto)

numero_entero = 2024
print(numero_entero)

# La función type() retorna el tipo de dato de la variable
edad = 26

print("Edad es un tipo de dato ", type(edad))

# Variable booleano True or False
acceso = False
print("Acceso es igual a ", acceso)

# Se pueden definir variables en una sola línea
nombre, apellido, edad = "Diana", "Delgadillo", 26
print("Hola, soy: ", nombre, apellido,". Tengo: ", edad, " años")

